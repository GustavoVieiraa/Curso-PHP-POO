        <h1>O que é um Objeto?</h1>
        <p>Um objeto é uma coisa material ou abstrata que pode ser percebida pelos sentidos e descrita por meio das suas características, comportamentos e estado atual.</p>
        <h1>Caracteristicas de um Objeto:</h1>
        <ul> <p>Todo Objeto tem:</p>
            <li>Atributos</li>
            <li>Métodos</li>
            <li>Estado</li>
        </ul>
        <p>Primeiro, antes de termos o nosso objeto, temos que criar a sua classe e atribuir a ela os atributos, metodos e estado que, quando ao <strong>instanciar</strong> os nossos objetos eles venham herdar esses atributos, metodos e estados.</p>
        <h1>Instanciar</h1>
        <p>Nós utilizamos as classes para <strong>instanciar</strong> objetos. Não transformamos uma classe em um objeto, geramos um objeto a partir de uma classe.</p>
        <h1>Classe</h1>
        <p>Uma classe define os atributos e métodos comuns que serão compartilhados por outros objetos.</p>
        <h1>Objeto</h1>
        <p>É uma instância de uma classe.</p>
