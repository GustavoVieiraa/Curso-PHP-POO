        <h1>Navegação pela Herança - Aprendido!</h1>
        <h1>Tipos de Herança</h1>
        <p>Herança de Implementação: É uma herança que ira implementar em suas classes.</p>
        <p>Herança para Diferença: É uma herança para diferença</p>
        <strong>Classe abstrata:</strong><p>Não pode ser instanciada. Só pode servir como progenitora.</p>
        <strong>Classe Método abstrato</strong><p>Declarado, mas não implementado na progenitora</p>
        <strong>Classe Final</strong><p>Não pode ser herdada por outra classe. Obrigatoriamente folha.</p>
        <strong>Método Final</strong><p>Não pode ser sobrescrito pelas suas sub-classes. Obrigatoriamente herdado.</p>
