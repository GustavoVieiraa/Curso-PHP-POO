        <h1>Polimorfismo</h1>
        <p>Permite que um mesmo nome represente vários comportamento diferentes</p>
        <p>Em uma metodo onde aplicamos Polimorfismo, temos que nós atentar a quantidade e os tipos dos parâmetros.</p>
        <h1>Tipos de Polimorfismo</h1>
        <p>Temos Sobreposição e Sobrecarga</p>
        <p>Polimorfismo de Sobreposição: Acontece quando substituímos um método de uma superclasse na sua subclasse, usando a mesma assinatura.</p>
